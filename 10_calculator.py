def add(n1, n2):
    return n1 + n2


def subtract(n1, n2):
    return n1 - n2


def multiply(n1, n2):
    return n1 * n2


def divide(n1, n2):
    return n1 / n2


operations = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide,
}


def calc():
    print(logo)
    num1 = float(input("What's the first number?: "))
    go_on = "y"
    while go_on == "y":
        for key in operations:
            print(key)
        operation_symbol = input("Pick an operation: ")
        num2 = float(input("What's the next number?: "))
        result = operations[operation_symbol](num1, num2)

        print(f"{num1} {operation_symbol} {num2} = {result}")

        go_on = input(f"Type 'y' to continue calculating with {result}, "
                      f"type 'n' to start a new calculating,"
                      f"type 'e' to exit the program: ")

        if go_on == "y":
            num1 = result
        elif go_on == "n":
            calc()


calc()

