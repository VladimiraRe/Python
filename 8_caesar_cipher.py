def caesar(t, sh, d):
    """ This is where you can securely encrypt your very secret messages. """
    cipher_text = ""
    message = "encoded"
    if d == "decode":
        sh *= -1
        message = "decoded"
    for i in t:
        if i not in alphabet:
            cipher_text += i
        else:
            ind = alphabet.index(i)
            cipher_text += alphabet[ind + sh]
    print(f"The {message} text is {cipher_text}")


